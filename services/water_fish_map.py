def lookup_water_fish(water_name: str | None) -> list[str] | None:
    """Find fish list for a water body by name.

    Returns list of fish names (matching Fish.name in fish_db.py) or None
    if the water body is not in the map.
    """
    if not water_name:
        return None
    name_lower = water_name.lower()
    for pattern, fish_list in WATER_FISH_MAP:
        if pattern in name_lower:
            return fish_list
    return None
